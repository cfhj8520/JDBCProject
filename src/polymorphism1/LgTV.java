package polymorphism1;

public class LgTV {
	public LgTV() {
		System.out.println("LgTV 객체 생성");
	}
	public void turnOn() {
		System.out.println("LgTV 전원 키기");
	}
	public void turnOff() {
		System.out.println("LgTV 전원 끄기");
		}
	public void soundUp() {
		System.out.println("LgTV 볼륨 키우기");
	}
	public void soundDown() {
		System.out.println("LgTV 볼륨 줄이기");
	}
}
